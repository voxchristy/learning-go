//Go is a statically typed language
//We can set types explicitly or implicitly
package main

func main() {
	//explicit typing
	var abc int = 42
	var str string = "Hello World"

	//implicit typing(use := without var, type is inferred from initial value)
	a := 42
	s := "Hello world"

	//explicit Constants
	const p int = 42

	//implicit Constants
	const q = "Hello World"

}
